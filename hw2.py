import pandas as pd
import numpy as np

# Создаем DataFrame
data = {
    "Имя": ["Алексей", "Мария", "Иван", "Екатерина", "Дмитрий", "Ольга", "Николай", "Анна", "Павел", "Татьяна"],
    "Математика": np.random.randint(2, 6, 10),
    "Физика": np.random.randint(2, 6, 10),
    "Химия": np.random.randint(2, 6, 10),
    "Биология": np.random.randint(2, 6, 10),
    "История": np.random.randint(2, 6, 10)
}

df = pd.DataFrame(data)

print(df.head())    # выводим первые несколько строк

print(f"Средний балл по математике: {df['Математика'].mean()}")
print(f"Средний балл по физике: {df['Физика'].mean()}")
print(f"Средний балл по химии: {df['Химия'].mean()}")
print(f"Средний балл по биологии: {df['Биология'].mean()}")
print(f"Средний балл по истории: {df['История'].mean()}")

print(f"Медиана по математике: {df['Математика'].median()}")
print(f"Медиана по физике: {df['Физика'].median()}")
print(f"Медиана по химии: {df['Химия'].median()}")
print(f"Медиана по биологии: {df['Биология'].median()}")
print(f"Медиана по истории: {df['История'].median()}")

Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print(f"Q1 для математики: {Q1_math}")
print(f"Q3 для математики: {Q3_math}")
print(f"IQR для математики: {IQR_math}\n")

print(f"Стандартное отклонение по математике: {df['Математика'].std()}")
print(f"Стандартное отклонение по физике: {df['Физика'].std()}")
print(f"Стандартное отклонение по химии: {df['Химия'].std()}")
print(f"Стандартное отклонение по биологии: {df['Биология'].std()}")
print(f"Стандартное отклонение по истории: {df['История'].std()}")

print("Средний балл по каждому предмету:")
print(df.mean(numeric_only=True), "\n")           # numeric_only=True - только числовые столбцы

# Вывод среднего балла каждого ученика
print("Средний балл каждого ученика:")
for index, row in df.iterrows():
    avg_score = row.iloc[1:].mean()  # Берем все предметы, кроме имени
    print(f"{row['Имя']}: {avg_score:.2f}")